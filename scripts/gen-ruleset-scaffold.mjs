// Generates git-ignored scaffold modules from canonical source files so their
// bytes are embedded in the compiled modules. Must run BEFORE tsc so imports
// resolve. Hard-fails when a verified shipped source is missing: generating
// from anything but the single canonical source would allow drift.
import { existsSync, readFileSync, writeFileSync } from "node:fs";

const source = new URL("../src/advanced-ruleset.py", import.meta.url);
const target = new URL("../src/ruleset-scaffold.ts", import.meta.url);
const configSource = new URL("../src/global-subagent-mcp-config.jsonc", import.meta.url);
const configTarget = new URL("../src/config-scaffold.ts", import.meta.url);

if (!existsSync(source)) {
  console.error("ERROR src/advanced-ruleset.py is absent; cannot generate src/ruleset-scaffold.ts");
  process.exit(1);
}
if (!existsSync(configSource)) {
  console.error("ERROR src/global-subagent-mcp-config.jsonc is absent; cannot generate src/config-scaffold.ts");
  process.exit(1);
}

const text = readFileSync(source, "utf8");
writeFileSync(
  target,
  "// GENERATED by scripts/gen-ruleset-scaffold.mjs from src/advanced-ruleset.py — DO NOT EDIT.\n" +
    `export const RULESET_SCAFFOLD: string = ${JSON.stringify(text)};\n`
);
console.log("Generated src/ruleset-scaffold.ts from src/advanced-ruleset.py");

const configText = readFileSync(configSource, "utf8");
writeFileSync(
  configTarget,
  "// GENERATED by scripts/gen-ruleset-scaffold.mjs from src/global-subagent-mcp-config.jsonc — DO NOT EDIT.\n" +
    `export const CONCURRENCY_SCAFFOLD: string = ${JSON.stringify(configText)};\n`
);
console.log("Generated src/config-scaffold.ts from src/global-subagent-mcp-config.jsonc");
