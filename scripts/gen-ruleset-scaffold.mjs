// Generates git-ignored src/ruleset-scaffold.ts from the canonical
// src/advanced-ruleset.py so the scaffold bytes are embedded in the compiled
// module (ruleset.ts imports RULESET_SCAFFOLD to recreate a deleted
// dist/advanced-ruleset.py at runtime). Must run BEFORE tsc so the import
// resolves. Hard-fails when the source is missing: the scaffold is a verified
// shipped part, and generating from anything but the single canonical .py
// would allow drift.
import { existsSync, readFileSync, writeFileSync } from "node:fs";

const source = new URL("../src/advanced-ruleset.py", import.meta.url);
const target = new URL("../src/ruleset-scaffold.ts", import.meta.url);

if (!existsSync(source)) {
  console.error("ERROR src/advanced-ruleset.py is absent; cannot generate src/ruleset-scaffold.ts");
  process.exit(1);
}

const text = readFileSync(source, "utf8");
writeFileSync(
  target,
  "// GENERATED by scripts/gen-ruleset-scaffold.mjs from src/advanced-ruleset.py — DO NOT EDIT.\n" +
    `export const RULESET_SCAFFOLD: string = ${JSON.stringify(text)};\n`
);
console.log("Generated src/ruleset-scaffold.ts from src/advanced-ruleset.py");
